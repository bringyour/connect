package connect_test

import (
	"context"
	"testing"

	"bringyour.com/connect"
	"github.com/go-playground/assert/v2"
	"github.com/stretchr/testify/require"
)

func TestPacketTransformer(t *testing.T) {

	t.Run("RewritePacketFromVPN and RewritePacketToVPN", func(t *testing.T) {
		ctx, cancel := context.WithCancel(context.Background())
		defer cancel()
		originalFromVPN := []byte{
			0x45, 0x00, 0x00, 0x40, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 0x00, 0x00, 0x7f, 0x00, 0x00, 0x01,
			0x7f, 0x00, 0x00, 0x01, 0xe3, 0xa5, 0x1f, 0x90, 0x6b, 0x9d, 0x68, 0x93, 0x00, 0x00, 0x00, 0x00,
			0xb0, 0x02, 0xff, 0xff, 0xfe, 0x34, 0x00, 0x00, 0x02, 0x04, 0x3f, 0xd8, 0x01, 0x03, 0x03, 0x06,
			0x01, 0x01, 0x08, 0x0a, 0x29, 0x02, 0xd5, 0x4c, 0x00, 0x00, 0x00, 0x00, 0x04, 0x02, 0x00, 0x00,
		}

		pt := connect.NewPacketTransformer(ctx)
		rewritten, err := pt.RewritePacketFromVPN(originalFromVPN, connect.TransferPath{})
		assert.Equal(t, nil, err)

		expectedFromVPN := []byte{
			0x45, 0x00, 0x00, 0x40, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 0xbb, 0xb6, 0x00, 0x00, 0x00, 0x01,
			0x7f, 0x00, 0x00, 0x01, 0xe3, 0xa5, 0x1f, 0x90, 0x6b, 0x9d, 0x68, 0x93, 0x00, 0x00, 0x00, 0x00,
			0xb0, 0x02, 0xff, 0xff, 0xa8, 0x20, 0x00, 0x00, 0x02, 0x04, 0x3f, 0xd8, 0x01, 0x03, 0x03, 0x06,
			0x01, 0x01, 0x08, 0x0a, 0x29, 0x02, 0xd5, 0x4c, 0x00, 0x00, 0x00, 0x00, 0x04, 0x02, 0x00, 0x00,
		}

		require.Equal(t, expectedFromVPN, rewritten)

		originalToVPN := []byte{
			0x45, 0x00, 0x00, 0x28, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 0x00, 0x00, 0x7f, 0x00, 0x00, 0x01,
			0x00, 0x00, 0x00, 0x01, 0x1f, 0x90, 0xe3, 0xa5, 0x00, 0x00, 0x00, 0x00, 0x6b, 0x9d, 0x68, 0x94,
			0x50, 0x14, 0x00, 0x00, 0xfe, 0x1c, 0x00, 0x00,
		}

		rewrittenToVPN, _, err := pt.RewritePacketToVPN(originalToVPN)
		require.NoError(t, err)

		expectedToVPN := []byte{
			0x45, 0x00, 0x00, 0x28, 0x00, 0x00, 0x40, 0x00, 0x40, 0x06, 0x3c, 0xce, 0x7f, 0x00, 0x00, 0x01,
			0x7f, 0x00, 0x00, 0x01, 0x1f, 0x90, 0xe3, 0xa5, 0x00, 0x00, 0x00, 0x00, 0x6b, 0x9d, 0x68, 0x94,
			0x50, 0x14, 0x00, 0x00, 0xda, 0x66, 0x00, 0x00,
		}

		require.Equal(t, expectedToVPN, rewrittenToVPN)

	})
}
